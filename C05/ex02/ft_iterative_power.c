#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	num;
	int	i;

	num = nb;
	i = 1;
	if (power < 0)
		return (0);
	if (nb == 0 && power == 0)
		return (1);
	while (i < power)
	{
		num = num * nb;
		i++;
	}
	if (nb < 0)
		num = -num;
	return (num);
}
